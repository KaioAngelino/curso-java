package joaomcode.aula022.revisao;

public class Aeroporto {

	private int id;
	private String nome;
	private String codigo;
	private Endereco endereco;

	public Aeroporto(int id, String nome, String codigo, Endereco endereco) {
		this.id = id;
		this.nome = nome;
		this.codigo = codigo;
		this.endereco = endereco;
	}

}
